function setCountry(){
	var val = $("#select_continent").find("option:selected").val();
	var index = $("#select_continent").find("option").index($("#select_continent").find("option:selected"));

	var name, value, optionHtml = "";

	if(index > 0){
		var tmpArr = ContinentListArray[val];

		$.each(tmpArr, function(idx){
			//console.log(tmpArr[idx]);
			var list = tmpArr[idx].split("|@|");

			name = list[0];
			value = list[1];

			optionHtml += '<option value="'+value+'">'+name+'</option>';
		});
		$("#select_country").html(optionHtml);
	}else{
		$("#select_country").html("<option>Country</option>");
	}
}

